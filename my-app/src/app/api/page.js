// Initialize the contract
const contract = new web3.eth.Contract(contractABI, contractAddress);

// Replace with the address and private key of the account you want to use to send the transaction
const accountAddress = "0xb77C6aB632D82346B56F872A199E08f5d2b5C61c";
const privateKey = "f951faf0875befc3c0c4b8c508ba8597d00f0f981a6f65527596a0207fca5355";

export default async function handler(req, res) {
  if (req.method !== 'GET') {
    res.status(405).json({ error: 'Only GET method is allowed' });
    return;
  }

  const { betId, startPrice, endPrice } = req.query;

  if (!betId || !startPrice || !endPrice) {
    res.status(400).json({ error: 'Missing required query parameters: betId, startPrice, endPrice' });
    return;
  }

  try {
    const nonce = await web3.eth.getTransactionCount(accountAddress, 'latest');

    const tx = {
      from: accountAddress,
      to: contractAddress,
      gas: 2000000,
      gasPrice: web3.utils.toWei('20', 'gwei'),
      nonce: nonce,
      data: contract.methods.settleBet(betId, startPrice, endPrice).encodeABI(),
      chainId: 11155111  // Sepolia chain ID
    };

    const signedTx = await web3.eth.accounts.signTransaction(tx, privateKey);
    const receipt = await web3.eth.sendSignedTransaction(signedTx.rawTransaction);

    res.status(200).json({ success: true, transactionHash: receipt.transactionHash, receipt });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
}
